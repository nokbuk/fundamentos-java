
public class Primitivos {

	public static void main(String[] args) {
		// Tipos primitivos (variáveis)
		// Tipo booleano(verdadeiro ou falso)
		boolean completo = true;
		//tipo char (1 caractere unicode 'aspas simples')
		char sexo = 'F';
		//Tipo byte (números inteiros)
		byte idade = 18;
		//tipo short (números inteiros)
		short numero1 = 1350;
		//padrão para números inteiros
		int numero2 = 312123;
		//tipo long (numeros inteiros) **** L
		long numero3 = 21474836470l;
		//tipo float (números não inteiros) *** f
		float nota1 = 5.6f;
		//tipo double (números não inteiros)
		double nota2 = 7.8; 
		//String (cadeia de caracte "aspas"
		String nome = "Marquinhos";
		//Object (aceita qualquer merda)

		System.out.println(completo);
		System.out.println(sexo);
		System.out.println(idade);
		System.out.println(numero1);
		System.out.println(numero2);
		System.out.println(numero3);
		System.out.println(nota1);
		System.out.println(nota2);
		System.out.println(nome);
		
	}

}
